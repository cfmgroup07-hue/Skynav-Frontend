// Get translation for a key
export const getTranslation = (key, language = 'en-US') => {
  const lang = language || 'en-US'
  const langCode = lang.split('-')[0]
  
  // Try full language code first
  if (translations[lang]) {
    return translations[lang][key] || key
  }
  
  // Try base language code
  if (translations[langCode]) {
    return translations[langCode][key] || key
  }
  
  // Fallback to English
  return translations['en-US'][key] || key
}

// Get all translations for a language
export const getTranslations = (language = 'en-US') => {
  const lang = language || 'en-US'
  const langCode = lang.split('-')[0]
  
  if (translations[lang]) {
    return translations[lang]
  }
  
  if (translations[langCode]) {
    return translations[langCode]
  }
  
  return translations['en-US']
}

